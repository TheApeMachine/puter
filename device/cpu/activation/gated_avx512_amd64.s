// func SwiGLUTensorsF32AVX512(dst, gate, up *float32, count int)
TEXT ·SwiGLUTensorsF32AVX512(SB), NOSPLIT, $0-32
	MOVQ dst+0(FP), DI
	MOVQ gate+8(FP), SI
	MOVQ up+16(FP), R8
	MOVQ count+24(FP), CX
	MOVQ $actX86ExpC<>(SB), AX
	VMOVSS (AX), X8
	VBROADCASTSS X8, Y8
	VMOVSS 4(AX), X9
	VBROADCASTSS X9, Y9
	VMOVSS 12(AX), X11
	VBROADCASTSS X11, Y11
	VMOVSS 16(AX), X12
	VBROADCASTSS X12, Y12
	VMOVSS 20(AX), X13
	VBROADCASTSS X13, Y13
	VMOVSS 24(AX), X14
	VBROADCASTSS X14, Y14
	VMOVSS 28(AX), X15
	VBROADCASTSS X15, Y15
	VMOVSS 32(AX), X16
	VBROADCASTSS X16, Y16
	VMOVSS 36(AX), X17
	VBROADCASTSS X17, Y17
	VPBROADCASTD actX86Bias127<>(SB), Y20
	VMOVSS actX86LogC<>+4(SB), X8
	VBROADCASTSS X8, Y26
	VXORPS Y15, Y15, Y15
swiglu_avx512_w8:
	CMPQ CX, $8
	JL swiglu_avx512_w4
	VMOVUPS (SI), Y0
	VMOVAPS Y0, Y10
	VMOVUPS (R8), Y11
	VXORPS Y0, Y0, Y1
	VSUBPS Y1, Y0, Y0
	VMULPS Y8, Y0, Y1
	VROUNDPS $8, Y1, Y1
	VMULPS Y1, Y9, Y2
	VSUBPS Y2, Y0, Y0
	VMOVAPS Y11, Y3
	VFMADD213PS Y3, Y0, Y11
	VMOVAPS Y12, Y3
	VFMADD213PS Y3, Y0, Y12
	VMOVAPS Y13, Y3
	VFMADD213PS Y3, Y0, Y13
	VMOVAPS Y14, Y3
	VFMADD213PS Y3, Y0, Y14
	VMOVAPS Y15, Y3
	VFMADD213PS Y3, Y0, Y15
	VMOVAPS Y16, Y3
	VFMADD213PS Y3, Y0, Y16
	VMOVAPS Y17, Y7
	VFMADD213PS Y7, Y0, Y17
	VCVTPS2DQ Y1, Y1
	VPADDD Y20, Y1, Y1
	VPSLLD $23, Y1, Y1
	VPADDD Y1, Y7, Y7
	VADDPS Y7, Y26, Y6
	VDIVPS Y6, Y26, Y7
	VMULPS Y10, Y7, Y7
	VMULPS Y11, Y7, Y7
	VMOVUPS Y7, (DI)
	ADDQ $32, SI
	ADDQ $32, R8
	ADDQ $32, DI
	SUBQ $8, CX
	JMP swiglu_avx512_w8
swiglu_avx512_w4:
	CMPQ CX, $4
	JL swiglu_avx512_w4_tail
	VMOVUPS (SI), X0
	VMOVAPS X0, X10
	VMOVUPS (R8), X11
	VXORPS X0, X0, X1
	VSUBPS X1, X0, X0
	VMULPS X8, X0, X1
	VROUNDPS $8, X1, X1
	VMULPS X1, X9, X2
	VSUBPS X2, X0, X0
	VMOVAPS X11, X3
	VFMADD213PS X3, X0, X11
	VMOVAPS X12, X3
	VFMADD213PS X3, X0, X12
	VMOVAPS X13, X3
	VFMADD213PS X3, X0, X13
	VMOVAPS X14, X3
	VFMADD213PS X3, X0, X14
	VMOVAPS X15, X3
	VFMADD213PS X3, X0, X15
	VMOVAPS X16, X3
	VFMADD213PS X3, X0, X16
	VMOVAPS X17, X7
	VFMADD213PS X7, X0, X17
	VCVTPS2DQ X1, X1
	VPADDD X20, X1, X1
	VPSLLD $23, X1, X1
	VPADDD X1, X7, X7
	VADDPS X7, X26, X6
	VDIVPS X6, X26, X7
	VMULPS X10, X7, X7
	VMULPS X11, X7, X7
	VMOVUPS X7, (DI)
	ADDQ $16, SI
	ADDQ $16, R8
	ADDQ $16, DI
	SUBQ $4, CX
	JMP swiglu_avx512_w4
swiglu_avx512_w4_tail:
	TESTQ CX, CX
	JZ swiglu_avx512_done
	MOVQ $1, AX
	SHLQ CL, AX
	DECQ AX
	KMOVQ AX, K7

	VMOVDQU32 (SI), K7, Y0
	VMOVAPS Y0, Y10
	VMOVDQU32 (R8), K7, Y11
	VXORPS Y0, Y0, Y1
	VSUBPS Y1, Y0, Y0
	VMULPS Y8, Y0, Y1
	VROUNDPS $8, Y1, Y1
	VMULPS Y1, Y9, Y2
	VSUBPS Y2, Y0, Y0
	VMOVAPS Y11, Y3
	VFMADD213PS Y3, Y0, Y11
	VMOVAPS Y12, Y3
	VFMADD213PS Y3, Y0, Y12
	VMOVAPS Y13, Y3
	VFMADD213PS Y3, Y0, Y13
	VMOVAPS Y14, Y3
	VFMADD213PS Y3, Y0, Y14
	VMOVAPS Y15, Y3
	VFMADD213PS Y3, Y0, Y15
	VMOVAPS Y16, Y3
	VFMADD213PS Y3, Y0, Y16
	VMOVAPS Y17, Y7
	VFMADD213PS Y7, Y0, Y17
	VCVTPS2DQ Y1, Y1
	VPADDD Y20, Y1, Y1
	VPSLLD $23, Y1, Y1
	VPADDD Y1, Y7, Y7
	VADDPS Y7, Y26, Y6
	VDIVPS Y6, Y26, Y7
	VMULPS Y10, Y7, Y7
	VMULPS Y11, Y7, Y7
	VMOVDQU32 Y7, K7, (DI)
	ADDQ $16, SI
	ADDQ $16, R8
	ADDQ $16, DI
swiglu_avx512_done:
	RET

