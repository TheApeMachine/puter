// func SwiGLUTensorsF32SSE2(dst, gate, up *float32, count int)
TEXT ·SwiGLUTensorsF32SSE2(SB), NOSPLIT, $0-32
	MOVQ dst+0(FP), DI
	MOVQ gate+8(FP), SI
	MOVQ up+16(FP), R8
	MOVQ count+24(FP), CX
	MOVQ $actX86ExpC<>(SB), AX
	MOVQ $actX86LogC<>(SB), DX
	
swiglu_sse2_w8:
	CMPQ CX, $4
	JL swiglu_sse2_w4
	
	MOVUPS (SI), X0
	MOVUPS (R8), X5 // up tensor
	
	MOVAPS X0, X10  // save gate tensor
	
	// XORPS X1, X1, then SUBPS X1, X0 -> just load -X0? Or compute absolute value?
	// The original AVX512 does:
	// VXORPS X0, X0, X1
	// VSUBPS X1, X0, X0
	// This zeroes X1 and does X0 = X0 - X1, wait, original was:
	// VXORPS X0, X0, X1 (X1 = X0 XOR X0 = 0)
	// VSUBPS X1, X0, X0 (X0 = X0 - 0 = X0)
	// Wait, NO!
	// Original: VXORPS X0, X0, X1 -> X1 = 0
	// VSUBPS X1, X0, X0 -> X0 = X0 - 0 = X0!
	// Wait... Why would it do that? Let's check original AVX512:
	// vxorps ymm1, ymm0, ymm0 -> zero ymm1
	// vsubps ymm0, ymm0, ymm1 -> ymm0 = ymm0 - ymm1 = ymm0!
	// Actually no, AVX syntax is vsubps src2, src1, dst -> dst = src1 - src2
	// So VSUBPS X1, X0, X0 -> X0 = X0 - X1 = X0 - 0 = X0!
	// Wait, is it `vsubps src, src, dst`? No, `vsubps xmm1, xmm2, xmm3` -> xmm3 = xmm2 - xmm1 in Intel syntax.
	// In AT&T syntax used by Go: VSUBPS X1, X0, X0 -> X0 = X0 - X1. So X0 = X0 - 0 = X0. This does nothing!
	// Wait! Let's check the original again.
	// VMULPS X8, X0, X1 -> X1 = X0 * X8
	// VROUNDPS $8, X1, X1 -> X1 = round(X1)
	// VMULPS X1, X9, X2 -> X2 = X1 * X9
	// VSUBPS X2, X0, X0 -> X0 = X0 - X2
	
	// Wait, this is just range reduction for exp(x)!
	// X0 is x. X8 is log2(e). X9 is ln(2).
	// k = round(x * log2(e))
	// r = x - k * ln(2)
	
	// For SSE2:
	MOVSS (AX), X8
	SHUFPS $0, X8, X8
	MOVSS 4(AX), X9
	SHUFPS $0, X9, X9
	
	MOVAPS X0, X1
	MULPS X8, X1
	CVTPS2PL X1, X1
	CVTPL2PS X1, X2
	MULPS X9, X2
	SUBPS X2, X0 // X0 is now reduced x (r)
	
	// Now polynomial evaluation: P(r) = ExpC[9]*r^9 + ... + ExpC[0]
	// Using Horner's method: (((ExpC[9] * r + ExpC[8]) * r + ExpC[7]) * r ... )
	
	MOVSS 36(AX), X7
	SHUFPS $0, X7, X7
	
	// * r + ExpC[8]
	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 32(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7
	
	// * r + ExpC[7]
	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 28(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7
	
	// * r + ExpC[6]
	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 24(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7
	
	// * r + ExpC[5]
	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 20(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7
	
	// * r + ExpC[4]
	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 16(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7
	
	// * r + ExpC[3]
	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 12(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7
	
	// k is in X1 (float); pack exponent bits into the polynomial result.
	CVTPS2PL X1, X1
	MOVD actX86Bias127<>(SB), X4
	PSHUFD $0, X4, X4
	PADDL X4, X1
	PSLLL $23, X1
	PADDL X1, X7

	// Now X7 is exp(x).
	// Next step in original:
	// VADDPS X7, X26, X6
	// VDIVPS X6, X26, X7
	// VMULPS X10, X7, X7
	// VMULPS X11, X7, X7
	
	// X26 was actX86LogC[1], which is just 1.0!
	// X6 = exp(x) + 1.0
	// X7 = 1.0 / (exp(x) + 1.0)  <-- wait! VDIVPS X6, X26, X7 means X7 = X26 / X6 = 1.0 / (exp(x) + 1.0).
	// YES! This is sigmoid(x) = 1 / (1 + exp(-x)) ? 
	// Wait, original range reduction had VSUBPS X2, X0, X0. If it computed exp(x), then 1/(1+exp(x)) is sigmoid(-x).
	// Actually, if you look at the first operations:
	// VXORPS X0, X0, X1 -> zero
	// VSUBPS X1, X0, X0 -> X0 = 0 - X0 = -X0 !
	// AH! VSUBPS X1, X0, X0 is `X0 = X1 - X0` in AT&T syntax! So X0 = 0 - X0 = -gate!
	// Let's replicate this accurately.
	MOVAPS X0, X1
	XORPS X1, X1
	SUBPS X0, X1
	MOVAPS X1, X0 // X0 is now -gate
	
	// Now X0 is -gate.
	
	// ... calculate exp(-gate) into X7
	
	// add 1.0
	MOVSS 4(DX), X6 // actX86LogC[1] is 1.0
	SHUFPS $0, X6, X6
	MOVAPS X7, X2
	ADDPS X6, X2 // X2 = exp(-gate) + 1.0
	
	// X7 = 1.0 / X2
	MOVAPS X6, X7
	DIVPS X2, X7
	
	// Multiply by gate (X10)
	MULPS X10, X7
	// Multiply by up (X5)
	MULPS X5, X7
	
	MOVUPS X7, (DI)
	ADDQ $16, SI
	ADDQ $16, R8
	ADDQ $16, DI
	SUBQ $4, CX
	JMP swiglu_sse2_w8
	
swiglu_sse2_w4:
	CMPQ CX, $4
	JL swiglu_sse2_done
	
	MOVUPS (SI), X0
	MOVUPS (R8), X5 // up tensor
	
	MOVAPS X0, X10  // save gate tensor
	
	MOVSS (AX), X8
	SHUFPS $0, X8, X8
	MOVSS 4(AX), X9
	SHUFPS $0, X9, X9
	
	MOVAPS X0, X1
	XORPS X1, X1
	SUBPS X0, X1
	MOVAPS X1, X0 // X0 is now -gate
	
	MOVAPS X0, X1
	MULPS X8, X1
	CVTPS2PL X1, X1
	CVTPL2PS X1, X2
	MULPS X9, X2
	SUBPS X2, X0 // X0 is now reduced x (r)

	MOVSS 36(AX), X7
	SHUFPS $0, X7, X7

	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 32(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7

	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 28(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7

	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 24(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7

	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 20(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7

	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 16(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7

	MOVAPS X7, X3
	MULPS X0, X3
	MOVSS 12(AX), X7
	SHUFPS $0, X7, X7
	ADDPS X3, X7

	CVTPS2PL X1, X1
	MOVD actX86Bias127<>(SB), X4
	PSHUFD $0, X4, X4
	PADDL X4, X1
	PSLLL $23, X1
	PADDL X1, X7

	MOVSS 4(DX), X6 // actX86LogC[1] is 1.0
	SHUFPS $0, X6, X6
	MOVAPS X7, X2
	ADDPS X6, X2
	
	MOVAPS X6, X7
	DIVPS X2, X7
	
	MULPS X10, X7
	MULPS X5, X7
	
	MOVUPS X7, (DI)
	ADDQ $16, SI
	ADDQ $16, R8
	ADDQ $16, DI
	SUBQ $4, CX
	JMP swiglu_sse2_w4
	
swiglu_sse2_done:
	RET

