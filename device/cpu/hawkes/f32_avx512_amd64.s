// func HawkesExpSumFloat32AVX512Asm(exponents *float32, count int) float32
TEXT ·HawkesExpSumFloat32AVX512Asm(SB), NOSPLIT, $64-20
	MOVQ exponents+0(FP), SI
	MOVQ count+8(FP), CX
	TESTQ CX, CX
	JZ   hawkes_sum_zero

	MOVQ $hawkesExpC<>(SB), AX
	VMOVSS (AX), X8
	VBROADCASTSS X8, Y8
	VMOVSS 4(AX), X9
	VBROADCASTSS X9, Y9
	VMOVSS 12(AX), X11
	VBROADCASTSS X11, Y11
	VMOVSS 16(AX), X12
	VBROADCASTSS X12, Y12
	VMOVSS 20(AX), X13
	VBROADCASTSS X13, Y13
	VMOVSS 24(AX), X14
	VBROADCASTSS X14, Y14
	VMOVSS 28(AX), X15
	VBROADCASTSS X15, Y15
	VMOVSS 32(AX), X16
	VBROADCASTSS X16, Y16
	VMOVSS 36(AX), X17
	VBROADCASTSS X17, Y17
	VPBROADCASTD hawkesExpBias127<>(SB), Y20
	VXORPS Y5, Y5, Y5

hawkes_sum_w16:
	CMPQ CX, $16
	JL   hawkes_sum_w8

	VMOVUPS (SI), Y0
	VMULPS Y8, Y0, Y1
	VROUNDPS $8, Y1, Y1
	VMULPS Y1, Y9, Y2
	VSUBPS Y2, Y0, Y0
	VMOVAPS Y11, Y7
	VFMADD213PS Y12, Y0, Y7
	VFMADD213PS Y13, Y0, Y7
	VFMADD213PS Y14, Y0, Y7
	VFMADD213PS Y15, Y0, Y7
	VFMADD213PS Y16, Y0, Y7
	VFMADD213PS Y17, Y0, Y7
	VCVTPS2DQ Y1, Y1
	VPADDD Y20, Y1, Y1
	VPSLLD $23, Y1, Y1
	VPADDD Y1, Y7, Y7
	VADDPS Y7, Y5, Y5

	ADDQ $64, SI
	SUBQ $16, CX
	JMP  hawkes_sum_w16

hawkes_sum_w8:
	CMPQ CX, $8
	JL   hawkes_sum_w4

	VMOVUPS (SI), Y0
	VMULPS Y8, Y0, Y1
	VROUNDPS $8, Y1, Y1
	VMULPS Y1, Y9, Y2
	VSUBPS Y2, Y0, Y0
	VMOVAPS Y11, Y7
	VFMADD213PS Y12, Y0, Y7
	VFMADD213PS Y13, Y0, Y7
	VFMADD213PS Y14, Y0, Y7
	VFMADD213PS Y15, Y0, Y7
	VFMADD213PS Y16, Y0, Y7
	VFMADD213PS Y17, Y0, Y7
	VCVTPS2DQ Y1, Y1
	VPADDD Y20, Y1, Y1
	VPSLLD $23, Y1, Y1
	VPADDD Y1, Y7, Y7
	VADDPS Y7, Y5, Y5

	ADDQ $32, SI
	SUBQ $8, CX
	JMP  hawkes_sum_w8

hawkes_sum_w4:
	CMPQ CX, $4
	JL   hawkes_sum_w4_tail

	VMOVUPS (SI), X0
	VMULPS X8, X0, X1
	VROUNDPS $8, X1, X1
	VMULPS X1, X9, X2
	VSUBPS X2, X0, X0
	VMOVAPS X11, X7
	VFMADD213PS X12, X0, X7
	VFMADD213PS X13, X0, X7
	VFMADD213PS X14, X0, X7
	VFMADD213PS X15, X0, X7
	VFMADD213PS X16, X0, X7
	VFMADD213PS X17, X0, X7
	VCVTPS2DQ X1, X1
	VPADDD X20, X1, X1
	VPSLLD $23, X1, X1
	VPADDD X1, X7, X7
	VADDPS X7, X5, X5

	ADDQ $16, SI
	SUBQ $4, CX
	JMP  hawkes_sum_w4

hawkes_sum_w4_tail:
	TESTQ CX, CX
	JZ   hawkes_sum_reduce

	MOVQ  CX, DX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	VMOVDQU32 (SI), K7, Y0
	VMULPS Y8, Y0, Y1
	VROUNDPS $8, Y1, Y1
	VMULPS Y1, Y9, Y2
	VSUBPS Y2, Y0, Y0
	VMOVAPS Y11, Y7
	VFMADD213PS Y12, Y0, Y7
	VFMADD213PS Y13, Y0, Y7
	VFMADD213PS Y14, Y0, Y7
	VFMADD213PS Y15, Y0, Y7
	VFMADD213PS Y16, Y0, Y7
	VFMADD213PS Y17, Y0, Y7
	VCVTPS2DQ Y1, Y1
	VPADDD Y20, Y1, Y1
	VPSLLD $23, Y1, Y1
	VPADDD Y1, Y7, Y7
	VXORPS Y24, Y24, Y24
	VBLENDMPS Y7, Y24, K7, Y24
	VADDPS Y24, Y5, Y5

hawkes_sum_reduce:
	LEAQ -64(SP), R15
	VMOVUPS Y5, (R15)
	VMOVUPS (R15), Y0
	VMOVUPS 16(R15), Y1
	VMOVUPS 32(R15), Y2
	VMOVUPS 48(R15), Y3
	VADDPS Y1, Y0, Y0
	VADDPS Y2, Y0, Y0
	VADDPS Y3, Y0, Y0
	VHADDPS Y0, Y0, Y0
	VHADDPS Y0, Y0, Y0
	VEXTRACTF128 $0, Y0, X0
	VMOVSS X0, ret+16(FP)
	RET

hawkes_sum_zero:
	XORPS X0, X0
	VMOVSS X0, ret+16(FP)
	RET

