#define WIDEN_FP16_8H(baseReg, dstY) \
	VMOVDQU X2, (baseReg); \
	VCVTPH2PS X2, dstY; \
	VPSRLDQ $8, X2, X3; \
	VCVTPH2PS X3, Y4; \
	VEXTRACTI128 $0, Y4, X4; \
	VINSERTF128 $1, X4, dstY, dstY

#define NARROW_FP16_Y8(dstReg) \
	VCVTPS2PH_Y0_X2; \
