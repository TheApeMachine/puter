#define WIDEN_FP16_8H(baseReg, dstY) \
	VMOVDQU X2, (baseReg); \
	VCVTPH2PS X2, dstY; \
	VPSRLDQ $8, X2, X3; \
	VCVTPH2PS X3, Y4; \
	VEXTRACTI128 $0, Y4, X4; \
	VINSERTF128 $1, X4, dstY, dstY

#define WIDEN_FP16_4H(baseReg, dstY) \
	VMOVDQU X2, (baseReg); \
	VCVTPH2PS X2, dstY

#define NARROW_FP16_Y8(dstReg) \
	VCVTPS2PH_Y0_X2; \
	VMOVDQU X2, (dstReg); \
	VEXTRACTF128 $1, Y0, X0; \
	VCVTPS2PH_X0_X2; \
	VMOVDQU X2, 8(dstReg)

#define NARROW_FP16_Y4(dstReg) \
	VCVTPS2PH_Y0_X2; \
	VMOVDQU X2, (dstReg)

#define AI_FP16_ACCUM_F64_Y(prodY, accumY) \
	VEXTRACTF128 $0, prodY, X8; \
	VCVTPS2PD X8, Y9; \
	VADDPD accumY, Y9, accumY; \
	VEXTRACTF128 $1, prodY, X8; \
	VCVTPS2PD X8, Y9; \
	VADDPD accumY, Y9, accumY

