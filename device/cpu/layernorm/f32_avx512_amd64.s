// func LayerNormSquaredDiffSumFloat32AVX512Asm(row *float32, count int, mean float32) float32
//
// Returns sum((row[i]-mean)²). f64 widen-square-accumulate matches losses MSE path.
TEXT ·LayerNormSquaredDiffSumFloat32AVX512Asm(SB), NOSPLIT, $0-28
	MOVQ row+0(FP), SI
	MOVQ count+8(FP), CX
	MOVSS mean+16(FP), X8
	VBROADCASTSS X8, Y8

	TESTQ CX, CX
	JZ   ln_ssd_avx512_zero

	VXORPD Y0, Y0, Y0

ln_ssd_avx512_w16:
	CMPQ CX, $16
	JL   ln_ssd_avx512_w8

	VMOVUPS Y1, (SI)
	VSUBPS  Y8, Y1, Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0
	VEXTRACTF128 $1, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0

	VMOVUPS Y1, 32(SI)
	VSUBPS  Y8, Y1, Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0
	VEXTRACTF128 $1, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0

	ADDQ $64, SI
	SUBQ $16, CX
	JMP  ln_ssd_avx512_w16

ln_ssd_avx512_w8:
	CMPQ CX, $8
	JL   ln_ssd_avx512_w4

	VMOVUPS Y1, (SI)
	VSUBPS  Y8, Y1, Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0
	VEXTRACTF128 $1, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0

	ADDQ $32, SI
	SUBQ $8, CX
	JMP  ln_ssd_avx512_w8

ln_ssd_avx512_w4:
	CMPQ CX, $4
	JL   ln_ssd_avx512_w4_tail

	VMOVUPS (SI), Y1
	VSUBPS  Y8, Y1, Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0
	VEXTRACTF128 $1, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y0, Y4, Y0

	ADDQ $16, SI
	SUBQ $4, CX
	JMP  ln_ssd_avx512_w4

ln_ssd_avx512_w4_tail:
	TESTQ CX, CX
	JZ   ln_ssd_avx512_reduce

	MOVQ  CX, DX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	VMOVDQU32 (SI), K7, Y1
	VSUBPS  Y8, Y1, Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VMULPD  Y4, Y4, Y4
	VADDPD  Y4, Y0, K7, Y0

ln_ssd_avx512_reduce:
	VHADDPD Y1, Y0, Y0
	VHADDPD Y1, Y1, Y1
	VEXTRACTF128 $0, Y1, X0
	CVTSD2SS X0, X0
	MOVSS X0, ret+24(FP)
	RET

ln_ssd_avx512_zero:
	XORPS X0, X0
	MOVSS X0, ret+24(FP)
	RET

// func LayerNormApplyRowFloat32AVX512Asm(out, row, scale, bias *float32, count int, mean, invStdDev float32)
TEXT ·LayerNormApplyRowFloat32AVX512Asm(SB), NOSPLIT, $0-48
	MOVQ out+0(FP), DI
	MOVQ row+8(FP), SI
	MOVQ scale+16(FP), R8
	MOVQ bias+24(FP), R9
	MOVQ count+32(FP), CX
	MOVSS mean+40(FP), X10
	VBROADCASTSS X10, Y10
	MOVSS invStdDev+44(FP), X11
	VBROADCASTSS X11, Y11

ln_apply_avx512_w16:
	CMPQ CX, $16
	JL   ln_apply_avx512_w8

	VMOVUPS (SI), Y1
	VMOVUPS (R8), Y2
	VMOVUPS (R9), Y3
	VSUBPS  Y10, Y1, Y4
	VMULPS  Y11, Y4, Y4
	VMULPS  Y2, Y4, Y4
	VADDPS  Y3, Y4, Y4
	VMOVUPS Y4, (DI)

	VMOVUPS 32(SI), Y1
	VMOVUPS 32(R8), Y2
	VMOVUPS 32(R9), Y3
	VSUBPS  Y10, Y1, Y4
	VMULPS  Y11, Y4, Y4
	VMULPS  Y2, Y4, Y4
	VADDPS  Y3, Y4, Y4
	VMOVUPS Y4, 32(DI)

	ADDQ $64, SI
	ADDQ $64, DI
	ADDQ $64, R8
	ADDQ $64, R9
	SUBQ $16, CX
	JMP  ln_apply_avx512_w16

ln_apply_avx512_w8:
	CMPQ CX, $8
	JL   ln_apply_avx512_w4

	VMOVUPS (SI), Y1
	VMOVUPS (R8), Y2
	VMOVUPS (R9), Y3
	VSUBPS  Y10, Y1, Y4
	VMULPS  Y11, Y4, Y4
	VMULPS  Y2, Y4, Y4
	VADDPS  Y3, Y4, Y4
	VMOVUPS Y4, (DI)

	ADDQ $32, SI
	ADDQ $32, DI
	ADDQ $32, R8
	ADDQ $32, R9
	SUBQ $8, CX
	JMP  ln_apply_avx512_w8

ln_apply_avx512_w4:
	CMPQ CX, $4
	JL   ln_apply_avx512_w4_tail

	VMOVUPS (SI), X1
	VMOVUPS (R8), X2
	VMOVUPS (R9), X3
	VSUBPS  X10, X1, X4
	VMULPS  X11, X4, X4
	VMULPS  X2, X4, X4
	VADDPS  X3, X4, X4
	VMOVUPS X4, (DI)

	ADDQ $16, SI
	ADDQ $16, DI
	ADDQ $16, R8
	ADDQ $16, R9
	SUBQ $4, CX
	JMP  ln_apply_avx512_w4

ln_apply_avx512_w4_tail:
	TESTQ CX, CX
	JZ   ln_apply_avx512_done

	MOVQ  CX, DX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	VMOVDQU32 (SI), K7, Y1
	VMOVDQU32 (R8), K7, Y2
	VMOVDQU32 (R9), K7, Y3
	VSUBPS  Y10, Y1, Y4
	VMULPS  Y11, Y4, Y4
	VMULPS  Y2, Y4, Y4
	VADDPS  Y3, Y4, Y4
	VMOVDQU32 Y4, K7, (DI)

ln_apply_avx512_done:
	RET
