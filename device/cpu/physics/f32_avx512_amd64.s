	JZ   lap1_avx512_done

lap1_avx512_w8:
	CMPQ CX, $8
	JL   lap1_avx512_w4

	VMOVUPS (R9), Y0
	VMOVUPS (R11), Y1
	VADDPS  Y1, Y0, Y0
	VMOVUPS (R10), Y1
	VADDPS  Y1, Y1, Y1
	VSUBPS  Y1, Y0, Y0
	VMULPS  Y7, Y0, Y0
	VMOVUPS Y0, (R8)

	ADDQ $32, R8
