#include "textflag.h"

#define VCVTPS2PH_X0_X2 WORD $0xC4E3; WORD $0x7D1D; BYTE $0xD0; BYTE $0x00

#define ACCUM_FP16_PRODUCTS_Z16(prodZ) \
	VEXTRACTI128 $0, prodZ, X10; \
	VEXTRACTI128 $1, prodZ, X11; \
	VCVTPH2PS X10, Y12; \
	VCVTPH2PS X11, Y13; \
	VADDPS Y12, Y0, Y0; \
	VADDPS Y13, Y0, Y0

#define ACCUM_FP16_PRODUCTS_X8(prodX) \
	VCVTPH2PS prodX, Y12; \
	VADDPS Y12, Y0, Y0

#define ACCUM_FP16_PRODUCTS_X4(prodX) \
	VCVTPH2PS prodX, X12; \
	VADDPS X12, X0, X0

// func DotFloat16AVX512Asm(left, right *uint16, count int) uint16
TEXT ·DotFloat16AVX512Asm(SB), NOSPLIT, $0-26
	MOVQ left+0(FP), SI
	MOVQ right+8(FP), DI
	MOVQ count+16(FP), CX

	TESTQ CX, CX
	JZ    dot_fp16_zero

	VXORPS Y0, Y0, Y0

dot_fp16_w16:
	CMPQ CX, $16
	JL   dot_fp16_w8

	VMOVUPH Z1, (SI)
	VMOVUPH Z2, (DI)
	VMULPH Z2, Z1, Z3
	ACCUM_FP16_PRODUCTS_Z16(Z3)

	ADDQ $32, SI
	ADDQ $32, DI
	SUBQ $16, CX
	JMP  dot_fp16_w16

dot_fp16_w8:
	CMPQ CX, $8
	JL   dot_fp16_w4

	VMOVUPH Y1, (SI)
	VMOVUPH Y2, (DI)
	VMULPH Y2, Y1, Y3
	VEXTRACTI128 $0, Y3, X10
	VEXTRACTI128 $1, Y3, X11
	VCVTPH2PS X10, Y12
	VCVTPH2PS X11, Y13
	VADDPS Y12, Y0, Y0
	VADDPS Y13, Y0, Y0

	ADDQ $16, SI
	ADDQ $16, DI
	SUBQ $8, CX
	JMP  dot_fp16_w8

dot_fp16_w4:
	CMPQ CX, $4
	JL   dot_fp16_reduce

	VMOVDQU X1, (SI)
	VMOVDQU X2, (DI)
	VMULPH X2, X1, X3
	ACCUM_FP16_PRODUCTS_X4(X3)

	ADDQ $8, SI
	ADDQ $8, DI
	SUBQ $4, CX
	JMP  dot_fp16_w4

dot_fp16_reduce:
	VEXTRACTF128 $1, Y0, X1
	VADDPS       X1, X0, X0
	VHADDPS      X0, X0, X0
	VHADDPS      X0, X0, X0

	TESTQ CX, CX
	JZ    dot_fp16_store

dot_fp16_scalar:
	MOVWLZX (SI), AX
	VMOVD X1, AX
	VCVTPH2PS X1, X1
	MOVWLZX (DI), DX
	VMOVD X2, DX
	VCVTPH2PS X2, X2
	VMULSS X2, X1, X1
	VADDSS X1, X0, X0

	ADDQ $2, SI
	ADDQ $2, DI
	DECQ CX
	JNZ  dot_fp16_scalar

dot_fp16_store:
	VCVTPS2PH_X0_X2
	MOVL  X2, AX
	MOVW  AX, ret+24(FP)
	RET

dot_fp16_zero:
	XORPS X0, X0
	VCVTPS2PH_X0_X2
	MOVL  X2, AX
	MOVW  AX, ret+24(FP)
	RET
