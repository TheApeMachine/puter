#include "textflag.h"
#include "../avx512_fp16_macros.inc"
#include "../f16c_fp16_macros.inc"

#define ACCUM_FP16_PRODUCTS_Z16(prodZ) \
	VEXTRACTI128 $0, prodZ, X10; \
	VEXTRACTI128 $1, prodZ, X11; \
	VCVTPH2PS X10, Y12; \
	VCVTPH2PS X11, Y13; \
	VADDPS Y12, Y0, Y0; \
	VADDPS Y13, Y0, Y0

#define ACCUM_FP16_PRODUCTS_X4(prodX) \
	VCVTPH2PS prodX, X12; \
	VADDPS X12, X0, X0

// func DotFloat16AVX512Asm(left, right *uint16, count int) uint16
TEXT ·DotFloat16AVX512Asm(SB), NOSPLIT, $0-26
	MOVQ left+0(FP), SI
	MOVQ right+8(FP), DI
	MOVQ count+16(FP), CX

	TESTQ CX, CX
	JZ    dot_fp16_zero

	VXORPS Y0, Y0, Y0

dot_fp16_w16:
	CMPQ CX, $16
	JL   dot_fp16_w8

	VMOVUPH_Y0_SI
	VMOVUPH_Y1_DI
	VMULPH_Y3_Y0_Y1
	VEXTRACTI128 $0, Y3, X10
	VEXTRACTI128 $1, Y3, X11
	VCVTPH2PS X10, Y12
	VCVTPH2PS X11, Y13
	VADDPS Y12, Y0, Y0
	VADDPS Y13, Y0, Y0

	ADDQ $16, SI
	ADDQ $16, DI

	VMOVUPH_Y0_SI
	VMOVUPH_Y1_DI
	VMULPH_Y3_Y0_Y1
	VEXTRACTI128 $0, Y3, X10
	VEXTRACTI128 $1, Y3, X11
	VCVTPH2PS X10, Y12
	VCVTPH2PS X11, Y13
	VADDPS Y12, Y0, Y0
	VADDPS Y13, Y0, Y0

	ADDQ $16, SI
	ADDQ $16, DI
	SUBQ $16, CX
	JMP  dot_fp16_w16

dot_fp16_w8:
	CMPQ CX, $8
	JL   dot_fp16_w4

	VMOVUPH_Y0_SI
	VMOVUPH_Y1_DI
	VMULPH_Y3_Y0_Y1
	VEXTRACTI128 $0, Y3, X10
	VEXTRACTI128 $1, Y3, X11
	VCVTPH2PS X10, Y12
	VCVTPH2PS X11, Y13
	VADDPS Y12, Y0, Y0
	VADDPS Y13, Y0, Y0

	ADDQ $16, SI
	ADDQ $16, DI
	SUBQ $8, CX
	JMP  dot_fp16_w8

dot_fp16_w4:
	CMPQ CX, $4
	JL   dot_fp16_reduce

	VMOVDQU X1, (SI)
	VMOVDQU X2, (DI)
	VMULPH_X3_X1_X2
	ACCUM_FP16_PRODUCTS_X4(X3)

	ADDQ $8, SI
	ADDQ $8, DI
	SUBQ $4, CX
	JMP  dot_fp16_w4

dot_fp16_reduce:
	VEXTRACTF128 $1, Y0, X1
	VADDPS       X1, X0, X0
	VHADDPS      X0, X0, X0
	VHADDPS      X0, X0, X0

	TESTQ CX, CX
	JZ    dot_fp16_store

dot_fp16_scalar:
	MOVWLZX (SI), AX
	VMOVD X1, AX
	VCVTPH2PS X1, X1
	MOVWLZX (DI), DX
	VMOVD X2, DX
	VCVTPH2PS X2, X2
	VMULSS X2, X1, X1
	VADDSS X1, X0, X0

	ADDQ $2, SI
	ADDQ $2, DI
	DECQ CX
	JNZ  dot_fp16_scalar

dot_fp16_store:
	FP16_NARROW_SCALAR_F32_X0_TO(ret+24(FP))
	RET

dot_fp16_zero:
	XORPS X0, X0
	FP16_NARROW_SCALAR_F32_X0_TO(ret+24(FP))
	RET
