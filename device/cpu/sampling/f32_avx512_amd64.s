// func GreedySampleFloat32AVX512Asm(logits *float32, count int) int32
TEXT ·GreedySampleFloat32AVX512Asm(SB), NOSPLIT, $64-20
	MOVQ logits+0(FP), SI
	MOVQ SI, BX
	MOVQ count+8(FP), CX
	TESTQ CX, CX
	JZ   greedy_avx512_zero

	CMPQ CX, $1
	JE   greedy_avx512_one

	MOVSS (SI), X0
	VBROADCASTSS X0, Z0
	ADDQ $4, SI
	DECQ CX

greedy_avx512_max_w16:
	CMPQ CX, $16
	JL   greedy_avx512_max_w8

	VMOVUPS (SI), Z1
	VMAXPS  Z1, Z0, Z0

	ADDQ $64, SI
	SUBQ $16, CX
	JMP  greedy_avx512_max_w16

greedy_avx512_max_w8:
	CMPQ CX, $8
	JL   greedy_avx512_max_w4

	LEAQ -64(SP), R15
	VMOVUPS Z0, (R15)
	VMOVUPS (R15), Y0
	VMOVUPS (SI), Y1
	VMAXPS  Y1, Y0, Y0
	VMOVUPS Y0, (R15)
	VMOVUPS (R15), Z0

	ADDQ $32, SI
	SUBQ $8, CX
	JMP  greedy_avx512_max_w8

greedy_avx512_max_w4:
	CMPQ CX, $4
	JL   greedy_avx512_max_w4_tail

	LEAQ -64(SP), R15
	VMOVUPS Z0, (R15)
	VMOVUPS (R15), Y0
	VMOVUPS (SI), Y1
	VMAXPS  Y1, Y0, Y0
	VMOVUPS Y0, (R15)
	VMOVUPS (R15), Z0

	ADDQ $16, SI
	SUBQ $4, CX
	JMP  greedy_avx512_max_w4

greedy_avx512_max_w4_tail:
	TESTQ CX, CX
	JZ   greedy_avx512_max_done

	MOVQ  CX, DX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	LEAQ -64(SP), R15
	VMOVUPS Z0, (R15)
	VMOVUPS (R15), Y0
	VMOVDQU32 (SI), K7, Y1
	VMAXPS  Y1, Y0, K7, Y0
	VMOVUPS Y0, (R15)
	VMOVUPS (R15), Z0

greedy_avx512_max_done:
	LEAQ -64(SP), R15
	VMOVUPS Z0, (R15)
	VMOVUPS (R15), Y0
	VMOVUPS 32(R15), Y1
	VMAXPS  Y1, Y0, Y0
	VHADDPS Y0, Y0, Y0
	VHADDPS Y0, Y0, Y0
	VEXTRACTF128 $0, Y0, X0

	MOVQ BX, SI
	MOVQ count+8(FP), CX
	XORQ R8, R8

greedy_avx512_find_w16:
	CMPQ CX, $16
	JL   greedy_avx512_find_w8

	VBROADCASTSS X0, Y1
	VMOVUPS (SI), Y2
	VCMPPS $0, Y1, Y2, K1
	KMOVQ K1, AX
	TESTQ AX, AX
	JNZ  greedy_avx512_found

	VMOVUPS 32(SI), Y2
	VCMPPS $0, Y1, Y2, K1
	KMOVQ K1, AX
	TESTQ AX, AX
	JNZ  greedy_avx512_found_hi8

	ADDQ $64, SI
	ADDQ $16, R8
	SUBQ $16, CX
	JMP  greedy_avx512_find_w16

greedy_avx512_found_hi8:
	BSFQ AX, DX
	ADDQ $8, DX
	ADDQ R8, DX
	MOVL DX, ret+16(FP)
	RET

greedy_avx512_find_w8:
	CMPQ CX, $8
	JL   greedy_avx512_find_w4

	VBROADCASTSS X0, Y1
	VMOVUPS (SI), Y2
	VCMPPS $0, Y1, Y2, K1
	KMOVQ K1, AX
	TESTQ AX, AX
	JNZ  greedy_avx512_found

	ADDQ $32, SI
	ADDQ $8, R8
	SUBQ $8, CX
	JMP  greedy_avx512_find_w8

greedy_avx512_find_w4:
	CMPQ CX, $4
	JL   greedy_avx512_find_w4_tail

	VBROADCASTSS X0, Y1
	VMOVUPS (SI), Y2
	VCMPPS $0, Y1, Y2, K1
	KMOVQ K1, AX
	TESTQ AX, AX
	JNZ  greedy_avx512_found

	ADDQ $16, SI
	ADDQ $4, R8
	SUBQ $4, CX
	JMP  greedy_avx512_find_w4

greedy_avx512_find_w4_tail:
	TESTQ CX, CX
	JZ   greedy_avx512_fail

	MOVQ  CX, DX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	VBROADCASTSS X0, Y1
	VMOVDQU32 (SI), K7, Y2
	VCMPPS $0, Y1, Y2, K7
	KMOVQ K7, AX
	TESTQ AX, AX
	JNZ  greedy_avx512_found

greedy_avx512_fail:
	MOVQ count+8(FP), AX
	DECQ AX
	MOVL AX, ret+16(FP)
	RET

greedy_avx512_found:
	BSFQ AX, DX
	ADDQ R8, DX
	MOVL DX, ret+16(FP)
	RET

greedy_avx512_one:
	XORL AX, AX
	MOVL AX, ret+16(FP)
	RET

greedy_avx512_zero:
	XORL AX, AX
	MOVL AX, ret+16(FP)
	RET

// func SamplingSoftmaxRowFloat32AVX512Asm(logits, out *float32, temperature float32, count int)
