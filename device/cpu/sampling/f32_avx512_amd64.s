	VFMADD213PS Y3, Y0, Y16
	VMOVAPS Y17, Y7
	VFMADD213PS Y7, Y0, Y17
	VCVTPS2DQ Y1, Y1
	VPADDD Y20, Y1, Y1
	VPSLLD $23, Y1, Y1
	VPADDD Y1, Y7, Y7
	VADDPS Y5, Y7, Y5
	VMOVUPS Y7, (DI)

	ADDQ $64, SI
	ADDQ $64, DI
	SUBQ $16, CX
	JMP  sam_softmax_exp_w16

sam_softmax_exp_w8:
	CMPQ CX, $8
	JL   sam_softmax_exp_w4

	VMOVUPS (SI), Y0
	VSUBPS Y6, Y0, Y0
	VDIVPS Y10, Y0, Y0
	VMAXPS Y4, Y0, Y0
	VMULPS Y8, Y0, Y1
	VROUNDPS $8, Y1, Y1
	VMULPS Y1, Y9, Y2
	VSUBPS Y2, Y0, Y0
	VMOVAPS Y11, Y3
	VFMADD213PS Y3, Y0, Y11
	VMOVAPS Y12, Y3
	VFMADD213PS Y3, Y0, Y12
