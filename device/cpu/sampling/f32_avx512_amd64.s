	SUBQ $4, CX
	JMP  sam_softmax_max_w4

sam_softmax_max_w4_tail:
	TESTQ CX, CX
	JZ   sam_softmax_max_reduce

	MOVQ  CX, DX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	LEAQ -64(SP), R15
	VMOVUPS Z0, (R15)
	VMOVUPS (R15), Y0
	VMOVDQU32 (SI), K7, Y1
	VMAXPS  Y1, Y0, K7, Y0
	VMOVUPS Y0, (R15)
	VMOVUPS (R15), Z0

sam_softmax_max_reduce:
	LEAQ -64(SP), R15
	VMOVUPS Z0, (R15)
	VMOVUPS (R15), Y0
	VMOVUPS 32(R15), Y1
	VMAXPS  Y1, Y0, Y0
	VHADDPS Y0, Y0, Y0
	VHADDPS Y0, Y0, Y0
	VEXTRACTF128 $0, Y0, X6

	MOVQ logits+0(FP), SI
	MOVQ count+24(FP), CX

	MOVQ $samExpC<>(SB), AX
	VMOVSS (AX), X8
	VBROADCASTSS X8, Y8
	VMOVSS 4(AX), X9
	VBROADCASTSS X9, Y9
	VMOVSS 12(AX), X11
	VBROADCASTSS X11, Y11
	VMOVSS 16(AX), X12
	VBROADCASTSS X12, Y12
	VMOVSS 20(AX), X13
	VBROADCASTSS X13, Y13
	VMOVSS 24(AX), X14
	VBROADCASTSS X14, Y14
	VMOVSS 28(AX), X15
	VBROADCASTSS X15, Y15
	VMOVSS 32(AX), X16
	VBROADCASTSS X16, Y16
	VMOVSS 36(AX), X17
	VBROADCASTSS X17, Y17
	VPBROADCASTD samExpBias127<>(SB), Y20
	VMOVSS samSoftmaxClamp<>(SB), X8
	VBROADCASTSS X8, Y4
	VBROADCASTSS X6, Y6
	VXORPS Y5, Y5, Y5

sam_softmax_exp_w16:
	CMPQ CX, $16
	JL   sam_softmax_exp_w8

	VMOVUPS (SI), Y0
	VSUBPS Y6, Y0, Y0
	VDIVPS Y10, Y0, Y0
	VMAXPS Y4, Y0, Y0
	VMULPS Y8, Y0, Y1
	VROUNDPS $8, Y1, Y1
	VMULPS Y1, Y9, Y2
