	VFMADD213PS Y7, Y0, Y17
	VCVTPS2DQ Y1, Y1
	VPADDD Y20, Y1, Y1
	VPSLLD $23, Y1, Y1
	VPADDD Y1, Y7, Y7
	VXORPS Y24, Y24, Y24
	VBLENDMPS Y7, Y24, K7, Y24
	VADDPS Y24, Y5, Y5
	VMOVDQU32 Y7, K7, (DI)

sam_softmax_exp_reduce:
	VHADDPS Y5, Y5, Y5
	VHADDPS Y5, Y5, Y5
	VEXTRACTF128 $0, Y5, X0
	XORPS X1, X1
	UCOMISS X0, X1
	JE    sam_softmax_done

	MOVSS samOneF32<>(SB), X8
	DIVSS X0, X8
	VBROADCASTSS X8, Y8

	MOVQ out+8(FP), DI
	MOVQ count+24(FP), CX

sam_softmax_scale_w16:
