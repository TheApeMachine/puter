	ADDQ $32, SI
	ADDQ $32, DI
	SUBQ $8, CX
	JMP  sam_softmax_exp_w8

sam_softmax_exp_w4:
	CMPQ CX, $4
	JL   sam_softmax_exp_w4_tail

	VMOVUPS (SI), X0
	VSUBPS X6, X0, X0
	VDIVPS X10, X0, X0
	VMAXPS X4, X0, X0
	VMULPS X8, X0, X1
	VROUNDPS $8, X1, X1
	VMULPS X1, X9, X2
	VSUBPS X2, X0, X0
	VMOVAPS X11, X3
	VFMADD213PS X3, X0, X11
