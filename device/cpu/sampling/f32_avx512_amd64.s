	MOVQ logits+0(FP), SI
	MOVQ out+8(FP), DI
	MOVSS temperature+16(FP), X10
	MOVQ count+24(FP), CX
	TESTQ CX, CX
	JZ   sam_softmax_done

	XORPS X11, X11
	UCOMISS X10, X11
	JNE  sam_softmax_temp_ok
	MOVSS samOneF32<>(SB), X10

sam_softmax_temp_ok:
	VBROADCASTSS X10, Y10

	MOVSS (SI), X0
	VBROADCASTSS X0, Z0
