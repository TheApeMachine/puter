#include "textflag.h"

// func RopePairsFloat32AVX512Asm(out, in, cos, sin *float32, pairs int)
// pairs = headDim/2. For each pair p:
//   out[2p]   = in[2p]*cos[p] - in[2p+1]*sin[p]
//   out[2p+1] = in[2p]*sin[p] + in[2p+1]*cos[p]
TEXT ·RopePairsFloat32AVX512Asm(SB), NOSPLIT, $0-40
	MOVQ out+0(FP), DI
	MOVQ in+8(FP), SI
	MOVQ cos+16(FP), R8
	MOVQ sin+24(FP), R9
	MOVQ pairs+32(FP), CX

rope_avx512_w8:
	CMPQ CX, $8
	JL   rope_avx512_w4

	VMOVUPS (SI), Y0

	VEXTRACTF128 $0, Y0, X0
	SHUFPS $136, X0, X1
	SHUFPS $221, X0, X2
	VMOVUPS (R8), X4
	VMOVUPS (R9), X5
	VMULPS X1, X4, X6
	VMULPS X2, X5, X7
	VSUBPS X7, X6, X6
	VMULPS X1, X5, X10
	VMULPS X2, X4, X11
	VADDPS X11, X10, X10
	MOVAPS X6, X12
	MOVAPS X6, X13
	UNPCKLPS X10, X12
	UNPCKHPS X10, X13
	VMOVUPS X12, (DI)
	VMOVUPS X13, 16(DI)
	ADDQ $16, R8
	ADDQ $16, R9

	VEXTRACTF128 $1, Y0, X0
	SHUFPS $136, X0, X1
	SHUFPS $221, X0, X2
	VMOVUPS (R8), X4
	VMOVUPS (R9), X5
	VMULPS X1, X4, X6
	VMULPS X2, X5, X7
	VSUBPS X7, X6, X6
	VMULPS X1, X5, X10
	VMULPS X2, X4, X11
	VADDPS X11, X10, X10
	MOVAPS X6, X12
	MOVAPS X6, X13
	UNPCKLPS X10, X12
	UNPCKHPS X10, X13
	VMOVUPS X12, 32(DI)
	VMOVUPS X13, 48(DI)
	ADDQ $16, R8
	ADDQ $16, R9

	ADDQ $32, SI
	ADDQ $64, DI
	SUBQ $8, CX
	JMP  rope_avx512_w8

rope_avx512_w4:
	CMPQ CX, $4
	JL   rope_avx512_w4_tail

	VMOVUPS (SI), X0
	SHUFPS $136, X0, X1
	SHUFPS $221, X0, X2
	VMOVUPS (R8), X4
	VMOVUPS (R9), X5
	VMULPS X1, X4, X6
	VMULPS X2, X5, X7
	VSUBPS X7, X6, X6
	VMULPS X1, X5, X10
	VMULPS X2, X4, X11
	VADDPS X11, X10, X10
	MOVAPS X6, X12
	MOVAPS X6, X13
	UNPCKLPS X10, X12
	UNPCKHPS X10, X13
	VMOVUPS X12, (DI)
	VMOVUPS X13, 16(DI)
	ADDQ $16, R8
	ADDQ $16, R9

	ADDQ $16, SI
	ADDQ $32, DI
	SUBQ $4, CX
	JMP  rope_avx512_w4

rope_avx512_w4_tail:
	TESTQ CX, CX
	JZ   rope_avx512_done

	MOVQ  CX, R10
	ADDQ  R10, R10
	MOVQ  R10, DX
	MOVQ  DX, CX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	VMOVDQU32 (SI), K7, Y0
	VMOVDQU32 (R8), K7, Y4
	VMOVDQU32 (R9), K7, Y5

	VEXTRACTF128 $0, Y0, X0
	SHUFPS $136, X0, X1
	SHUFPS $221, X0, X2
	VEXTRACTF128 $0, Y4, X4
	VEXTRACTF128 $0, Y5, X5
	VMULPS X1, X4, X6
	VMULPS X2, X5, X7
	VSUBPS X7, X6, X6
	VMULPS X1, X5, X10
	VMULPS X2, X4, X11
	VADDPS X11, X10, X10
	MOVAPS X6, X12
	MOVAPS X6, X13
	UNPCKLPS X10, X12
	UNPCKHPS X10, X13
	VINSERTF128 $0, X12, Y15, Y15
	VINSERTF128 $1, X13, Y15, Y15
	VMOVDQU32 Y15, K7, (DI)

rope_avx512_done:
	RET
