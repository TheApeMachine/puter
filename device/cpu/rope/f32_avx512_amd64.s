#include "textflag.h"

// func RopePairsFloat32AVX512Asm(out, in, cos, sin *float32, pairs int)
TEXT ·RopePairsFloat32AVX512Asm(SB), NOSPLIT, $0-40
	MOVQ out+0(FP), DI
	MOVQ in+8(FP), SI
	MOVQ cos+16(FP), R8
	MOVQ sin+24(FP), R9
	MOVQ pairs+32(FP), CX

rope_avx512_w4:
	CMPQ CX, $4
	JL   rope_avx512_tail

	VMOVUPS (SI), Y0

	VEXTRACTF128 $0, Y0, X0
	VSHUFPS $0x88, X0, X0, X1
	VSHUFPS $0xDD, X0, X0, X2
	VMOVUPS (R8), X4
	VSHUFPS $0x44, X4, X4, X4
	VMOVUPS (R9), X5
	VSHUFPS $0x44, X5, X5, X5
	VMULPS  X1, X4, X6
	VMULPS  X2, X5, X7
	VSUBPS  X7, X6, X6
	VMULPS  X1, X5, X10
	VMULPS  X2, X4, X11
	VADDPS  X11, X10, X10
	VUNPCKLPS X10, X6, X6
	VMOVUPS X6, (DI)
	ADDQ $16, R8
	ADDQ $16, R9
	ADDQ $16, DI

	VEXTRACTF128 $1, Y0, X0
	VSHUFPS $0x88, X0, X0, X1
	VSHUFPS $0xDD, X0, X0, X2
	VMOVUPS (R8), X4
	VSHUFPS $0x44, X4, X4, X4
	VMOVUPS (R9), X5
	VSHUFPS $0x44, X5, X5, X5
	VMULPS  X1, X4, X6
	VMULPS  X2, X5, X7
	VSUBPS  X7, X6, X6
	VMULPS  X1, X5, X10
	VMULPS  X2, X4, X11
	VADDPS  X11, X10, X10
	VUNPCKLPS X10, X6, X6
	VMOVUPS X6, (DI)
	ADDQ $16, R8
	ADDQ $16, R9
	ADDQ $16, DI

	ADDQ $32, SI
	SUBQ $4, CX
	JMP  rope_avx512_w4

rope_avx512_tail:
	TESTQ CX, CX
	JZ   rope_avx512_done

rope_avx512_scalar:
	VMOVSS (SI), X0
	VMOVSS 4(SI), X1
	VMOVSS (R8), X4
	VMOVSS (R9), X5
	VMULSS X0, X4, X6
	VMULSS X1, X5, X7
	VSUBSS X7, X6, X6
	VMULSS X0, X5, X10
	VMULSS X1, X4, X11
	VADDSS X11, X10, X10
	VMOVSS X6, (DI)
	VMOVSS X10, 4(DI)

	ADDQ $8, SI
	ADDQ $8, DI
	ADDQ $4, R8
	ADDQ $4, R9
	DECQ CX
	JNZ  rope_avx512_scalar

rope_avx512_done:
	VZEROUPPER
	RET
