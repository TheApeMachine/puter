#include "textflag.h"
#include "../avx512_fp16_macros.inc"

DATA geomCouplingFP16AbsMask16AVX512<>+0(SB)/2, $0x7fff
DATA geomCouplingFP16Eps16AVX512<>+0(SB)/2, $0x211f
GLOBL geomCouplingFP16AbsMask16AVX512<>(SB), RODATA|NOPTR, $2
GLOBL geomCouplingFP16Eps16AVX512<>(SB), RODATA|NOPTR, $2

#define COUPLING_FP16_X8 \
	VPAND  X14, X0, X2; \
	VPAND  X14, X1, X3; \
	VMULPH_X3_X2_X4; \
	VSQRTPH_X5_X4; \
	VMULPH_X1_X0_X7; \
	VMULPH_X5_X5_X6; \
	VDIVPH_X6_X7_X8; \
	VMAXPH_X5_X13_X10; \
	VPCMPEQW X5, X10, X11; \
	VPAND  X11, X8, X0

#define COUPLING_FP16_Y8 \
	VPAND  Y14, Y0, Y0; \
	VPAND  Y14, Y1, Y1; \
	VMULPH_Y3_Y2_Y4; \
	VSQRTPH_Y5_Y4; \
	VMULPH_Y1_Y0_Y7; \
	VMULPH_Y5_Y5_Y6; \
	VDIVPH_Y6_Y7_Y8; \
	VMAXPH_Y5_Y13_Y10; \
	VPCMPEQW Y5, Y10, Y11; \
	VPAND  Y11, Y8, Y8; \
	VMOVDQU Y8, Y0

#define COUPLING_FP16_SCALAR \
	MOVWLZX (SI), AX; \
	MOVW AX, 0(SP); \
	VPBROADCASTW 0(SP), X0; \
	MOVWLZX (R8), AX; \
	MOVW AX, 8(SP); \
	VPBROADCASTW 8(SP), X1; \
	COUPLING_FP16_X8

// func PhaseCouplingFloat16AVX512Asm(dst, left, right *uint16, count int)
TEXT ·PhaseCouplingFloat16AVX512Asm(SB), NOSPLIT, $16-32
	MOVQ destination+0(FP), DI
	MOVQ leftGrowth+8(FP), SI
	MOVQ rightGrowth+16(FP), R8
	MOVQ count+24(FP), CX

	VPBROADCASTW geomCouplingFP16AbsMask16AVX512<>(SB), Y14
	VPBROADCASTW geomCouplingFP16Eps16AVX512<>(SB), Y13
	VPBROADCASTW geomCouplingFP16AbsMask16AVX512<>(SB), X14
	VPBROADCASTW geomCouplingFP16Eps16AVX512<>(SB), X13

pcfp16_avx512_w16:
	CMPQ CX, $16
	JL   pcfp16_avx512_w8

	VMOVUPH_Y0_SI
	VMOVUPH_Y1_R8
	COUPLING_FP16_Y8
	STORE_Y0_16H_DI

	ADDQ $16, SI
	ADDQ $16, R8
	ADDQ $16, DI

	VMOVUPH_Y0_SI
	VMOVUPH_Y1_R8
	COUPLING_FP16_Y8
	STORE_Y0_16H_DI

	ADDQ $16, SI
	ADDQ $16, R8
	ADDQ $16, DI
	SUBQ $16, CX
	JMP  pcfp16_avx512_w16

pcfp16_avx512_w8:
	CMPQ CX, $8
	JL   pcfp16_avx512_tail

	VMOVUPH_X0_SI
	VMOVUPH_X1_R8
	COUPLING_FP16_X8
	STORE_X0_8H_DI

	ADDQ $16, SI
	ADDQ $16, R8
	ADDQ $16, DI
	SUBQ $8, CX
	JMP  pcfp16_avx512_w8

pcfp16_avx512_tail:
	TESTQ CX, CX
	JZ   pcfp16_avx512_done

pcfp16_avx512_scalar:
	COUPLING_FP16_SCALAR
	VMOVD X0, AX
	MOVW  AX, (DI)
	ADDQ  $2, SI
	ADDQ  $2, R8
	ADDQ  $2, DI
	DECQ  CX
	JNZ  pcfp16_avx512_scalar

pcfp16_avx512_done:
	RET
