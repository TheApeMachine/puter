#include "textflag.h"
#include "../avx512_fp16_macros.inc"

DATA l1FP16AbsHalfAVX512<>+0(SB)/2, $0x7fff
GLOBL l1FP16AbsHalfAVX512<>(SB), RODATA|NOPTR, $2

#define ACCUM_FP16_SUM_Y10 \
	VEXTRACTI128 $0, Y10, X10; \
	VEXTRACTI128 $1, Y10, X11; \
	VCVTPH2PS X10, Y12; \
	VCVTPH2PS X11, Y13; \
	VADDPS Y12, Y0, Y0; \
	VADDPS Y13, Y0, Y0

// func L1NormFloat16AVX512Asm(src *uint16, count int) float32
TEXT ·L1NormFloat16AVX512Asm(SB), NOSPLIT, $0-20
	MOVQ src+0(FP), SI
	MOVQ count+8(FP), CX

	TESTQ CX, CX
	JZ    l1_fp16_avx512_zero

	VPBROADCASTW l1FP16AbsHalfAVX512<>(SB), Y14
	VPBROADCASTW l1FP16AbsHalfAVX512<>(SB), X14
	VXORPS Y0, Y0, Y0

l1_fp16_avx512_w16:
	CMPQ CX, $16
	JL    l1_fp16_avx512_w8

	VMOVUPH_Y0_SI
	VPAND Y14, Y0, Y0
	VPXORD Y10, Y10, Y10
	VADDPH_Y10_Y0_Y10
	ACCUM_FP16_SUM_Y10

	ADDQ $16, SI

	VMOVUPH_Y0_SI
	VPAND Y14, Y0, Y0
	VPXORD Y10, Y10, Y10
	VADDPH_Y10_Y0_Y10
	ACCUM_FP16_SUM_Y10

	ADDQ $16, SI
	SUBQ $16, CX
	JMP  l1_fp16_avx512_w16

l1_fp16_avx512_w8:
	CMPQ CX, $8
	JL    l1_fp16_avx512_w4

	VMOVUPH_Y0_SI
	VPAND Y14, Y0, Y0
	VPXORD Y10, Y10, Y10
	VADDPH_Y10_Y0_Y10
	ACCUM_FP16_SUM_Y10

	ADDQ $16, SI
	SUBQ $8, CX
	JMP  l1_fp16_avx512_w8

l1_fp16_avx512_w4:
	CMPQ CX, $4
	JL    l1_fp16_avx512_reduce

	VMOVDQU X1, (SI)
	VPAND X14, X1, X1
	VPXORD X10, X10, X10
	VADDPH_X10_X1_X10
	VCVTPH2PS X10, X12
	VADDPS X12, X0, X0

	ADDQ $8, SI
	SUBQ $4, CX
	JMP  l1_fp16_avx512_w4

l1_fp16_avx512_reduce:
	VEXTRACTF128 $1, Y0, X1
	VADDPS       X1, X0, X0
	VHADDPS      X0, X0, X0
	VHADDPS      X0, X0, X0

	TESTQ CX, CX
	JZ    l1_fp16_avx512_store

l1_fp16_avx512_scalar:
	MOVWLZX (SI), AX
	ANDL  $0x7fff, AX
	VMOVD X1, AX
	VCVTPH2PS X1, X1
	VADDSS X1, X0, X0

	ADDQ $2, SI
	DECQ CX
	JNZ  l1_fp16_avx512_scalar

l1_fp16_avx512_store:
	MOVSS X0, ret+16(FP)
	RET

l1_fp16_avx512_zero:
	XORPS X0, X0
	MOVSS X0, ret+16(FP)
	RET
