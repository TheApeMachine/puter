// func SumFloat32AVX512Asm(src *float32, count int) float32
TEXT ·SumFloat32AVX512Asm(SB), NOSPLIT, $0-20
	MOVQ src+0(FP), SI
	MOVQ count+8(FP), CX
	TESTQ CX, CX
	JZ   sum_avx512_zero

	VXORPD Y0, Y0, Y0

sum_avx512_w16:
	CMPQ CX, $16
	JL   sum_avx512_w8

	VMOVUPS (SI), Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VADDPD  Y0, Y4, Y0
	VEXTRACTF128 $1, Y2, X3
	VCVTPS2PD X3, Y4
	VADDPD  Y0, Y4, Y0

	VMOVUPS 32(SI), Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VADDPD  Y0, Y4, Y0
	VEXTRACTF128 $1, Y2, X3
	VCVTPS2PD X3, Y4
	VADDPD  Y0, Y4, Y0

	ADDQ $64, SI
	SUBQ $16, CX
	JMP  sum_avx512_w16

sum_avx512_w8:
	CMPQ CX, $8
	JL   sum_avx512_w4

	VMOVUPS (SI), Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VADDPD  Y0, Y4, Y0
	VEXTRACTF128 $1, Y2, X3
	VCVTPS2PD X3, Y4
	VADDPD  Y0, Y4, Y0

	ADDQ $32, SI
	SUBQ $8, CX
	JMP  sum_avx512_w8

sum_avx512_w4:
	CMPQ CX, $4
	JL   sum_avx512_w4_tail

	VMOVUPS (SI), X2
	VCVTPS2PD X2, Y3
	VADDPD  Y0, Y3, Y0

	ADDQ $16, SI
	SUBQ $4, CX
	JMP  sum_avx512_w4

sum_avx512_w4_tail:
	TESTQ CX, CX
	JZ   sum_avx512_reduce

	MOVQ  CX, DX
	MOVQ  $1, AX
	SHLQ  CL, AX
	DECQ  AX
	KMOVQ AX, K7

	VMOVDQU32 (SI), K7, Y2
	VEXTRACTF128 $0, Y2, X3
	VCVTPS2PD X3, Y4
	VADDPD  Y4, Y0, K7, Y0

sum_avx512_reduce:
	VHADDPD Y1, Y0, Y0
	VHADDPD Y1, Y1, Y1
	VEXTRACTF128 $0, Y1, X0
	CVTSD2SS X0, X0
	MOVSS X0, ret+16(FP)
	RET

sum_avx512_zero:
	XORPS X0, X0
	MOVSS X0, ret+16(FP)
	RET

