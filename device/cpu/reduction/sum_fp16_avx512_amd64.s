	SUBQ $4, CX
	JMP  sum_fp16_w4

sum_fp16_reduce:
	VEXTRACTF128 $1, Y0, X1
	VADDPS       X1, X0, X0
	VHADDPS      X0, X0, X0
	VHADDPS      X0, X0, X0

	TESTQ CX, CX
	JZ    sum_fp16_store

sum_fp16_scalar:
