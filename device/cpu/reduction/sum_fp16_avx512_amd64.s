#include "textflag.h"

#define VCVTPS2PH_X0_X2 WORD $0xC4E3; WORD $0x7D1D; BYTE $0xD0; BYTE $0x00

#define ACCUM_FP16_SUM_Z16(srcZ) \
	VEXTRACTI128 $0, srcZ, X10; \
	VEXTRACTI128 $1, srcZ, X11; \
	VCVTPH2PS X10, Y12; \
	VCVTPH2PS X11, Y13; \
	VADDPS Y12, Y0, Y0; \
	VADDPS Y13, Y0, Y0

#define ACCUM_FP16_SUM_X4(srcX) \
	VCVTPH2PS srcX, X12; \
	VADDPS X12, X0, X0

// func SumFloat16AVX512Asm(src *uint16, count int) uint16
TEXT ·SumFloat16AVX512Asm(SB), NOSPLIT, $0-18
