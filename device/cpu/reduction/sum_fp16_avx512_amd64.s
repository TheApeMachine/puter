	DECQ CX
	JNZ  sum_fp16_scalar

sum_fp16_store:
	VCVTPS2PH_X0_X2
	MOVL  X2, AX
	MOVW  AX, ret+16(FP)
	RET

sum_fp16_zero:
	XORPS X0, X0
	VCVTPS2PH_X0_X2
	MOVL  X2, AX
	MOVW  AX, ret+16(FP)
	RET
