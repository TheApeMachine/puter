static inline uint dropout_jump_apply(uint value, uint power) {
    uint result = 0u;
    for (uint bit = 0u; bit < 32u; bit++) {
        if ((value & (1u << bit)) != 0u) {
            result ^= dropoutXorshiftPowers[power * 32u + bit];
        }
    }
    return result;
}

static inline uint dropout_xorshift_advance(uint seed, uint steps) {
    uint value = seed;
    uint power = 0u;
    while (steps != 0u) {
        if ((steps & 1u) != 0u) {
            value = dropout_jump_apply(value, power);
        }
        steps >>= 1u;
        power++;
    }
    return value;
}

